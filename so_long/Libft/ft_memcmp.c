#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned int	i;
	unsigned char	*s3;
	unsigned char	*s4;

	s3 = (unsigned char *)s1;
	s4 = (unsigned char *)s2;
	i = 0;
	while (n > 0)
	{
		if (s3[i] != s4[i])
			return (s3[i] - s4[i]);
		i++;
		n--;
	}
	return (0);
}
